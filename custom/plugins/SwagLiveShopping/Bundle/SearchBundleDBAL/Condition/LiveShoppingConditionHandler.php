namespace SwagLiveShopping\Bundle\SearchBundleDBAL\Condition;

use Shopware\Bundle\SearchBundle\ConditionInterface;
use Shopware\Bundle\SearchBundleDBAL\ConditionHandlerInterface;
use Shopware\Bundle\SearchBundleDBAL\QueryBuilder;
use Shopware\Bundle\StoreFrontBundle\Struct\ShopContextInterface;
use SwagLiveShopping\Bundle\SearchBundle\Condition\LiveShoppingCondition;
use SwagLiveShopping\Bundle\SearchBundleDBAL\AbstractLiveShoppingHandler;

class LiveShoppingConditionHandler extends AbstractLiveShoppingHandler implements ConditionHandlerInterface
{
    /**
     * {@inheritdoc}
     */
    public function supportsCondition(ConditionInterface $condition)
    {
        return $condition instanceof LiveShoppingCondition;
    }

    /**
     * {@inheritdoc}
     */
    public function generateCondition(
        ConditionInterface $condition,
        QueryBuilder $query,
        ShopContextInterface $context
    ) {
        $this->joinTable($query);
        $query->andWhere('liveShopping.id IS NOT NULL');
    }
}
